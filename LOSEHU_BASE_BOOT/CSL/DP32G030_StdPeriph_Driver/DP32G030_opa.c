#include "DP32G030_opa.h"

/****************************************************************************************************************************************** 
* 函数名称: OPA0_Open()
* 功能说明:	运放0打开
* 输    入: 无	   			
* 输    出: 无
* 注意事项: 无
******************************************************************************************************************************************/
void OPA0_Open(void)
{
	SYS->OPA_CFG |= 0x01 << SYS_OPA_CFG_OPA0_EN_POS;
}

/****************************************************************************************************************************************** 
* 函数名称: OPA0_Close()
* 功能说明:	运放0关闭
* 输    入: 无	   			
* 输    出: 无
* 注意事项: 无
******************************************************************************************************************************************/
void OPA0_Close(void)
{
	SYS->OPA_CFG &= ~(0x01 << SYS_OPA_CFG_OPA0_EN_POS);
}

